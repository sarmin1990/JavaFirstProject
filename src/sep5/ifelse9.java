package sep5;

public class ifelse9 {
    //if temperature is below 30 degree its cold
    //if temperature is below 60 degree its spring
    //if temperature is over 80 degree its hot
    public static void main(String [] args){
        int temp=45;
        if(temp<30){
            System.out.println("its cold");
        }else if(temp<60){
            System.out.println("its spring");
        }else if(temp>80){
            System.out.println("its hot");
        }


    }
}
